uint8_t command = cmd_zero;

void USART1_IRQHandler(void)                	//串口1中断服务程序
{
	u8 Res;

	if(USART_GetITStatus(USART1, USART_IT_RXNE) != RESET)  //接收中断(接收到的数据必须是0x0d 0x0a结尾)
	{
		Res =USART_ReceiveData(USART1);	//读取接收到的数据
		
		if((USART_RX_STA&0x8000)==0)//接收未完成
		{
			if(USART_RX_STA&0x4000)//接收到了0x0d
			{
				if(Res!=0x0a)
					USART_RX_STA=0;//接收错误,重新开始
				else
				{
					USART_RX_STA|=0x8000;	//接收完成了
					if( USART_RX_BUF[0]=='c' && USART_RX_BUF[1]=='m' && USART_RX_BUF[2]=='d' )
					{
						switch(USART_RX_BUF[4])
						{
							case 'o':	//伸出 cmd_out
								command = cmd_out;
								break;
							
							case 'b':	//缩回 cmd_back
								command = cmd_back;
								break;

							default:
								command = cmd_zero;
								break;
						}
					}
				}
				
			}
			else //还没收到0X0D
			{	
				if(Res==0x0d)
					USART_RX_STA|=0x4000;
				else
				{
					USART_RX_BUF[USART_RX_STA&0X3FFF]=Res ;
					USART_RX_STA++;
					if(USART_RX_STA>(USART_REC_LEN-1))USART_RX_STA=0;//接收数据错误,重新开始接收	  
				}		 
			}
		}   		 
    } 

}


//波特率115200
void USART2_Config(void)
{
 	GPIO_InitTypeDef GPIO_InitStructure;
	USART_InitTypeDef USART_InitStructure;
	NVIC_InitTypeDef NVIC_InitStructure; 
	
	//时钟使能
	RCC_APB1PeriphClockCmd(RCC_APB1Periph_USART2, ENABLE);
	RCC_APB2PeriphClockCmd(RCC_APB2Periph_GPIOA, ENABLE);
	
	//GPIO
	GPIO_InitStructure.GPIO_Pin = GPIO_Pin_2;
	GPIO_InitStructure.GPIO_Mode = GPIO_Mode_AF_PP;
	GPIO_InitStructure.GPIO_Speed = GPIO_Speed_50MHz;
	GPIO_Init(GPIOA, &GPIO_InitStructure);
	
	GPIO_InitStructure.GPIO_Pin = GPIO_Pin_3;
	GPIO_InitStructure.GPIO_Mode = GPIO_Mode_IN_FLOATING;
	GPIO_Init(GPIOA, &GPIO_InitStructure);
	
	//串口配置 
	USART_InitStructure.USART_BaudRate = 115200;
	USART_InitStructure.USART_WordLength = USART_WordLength_8b;
	USART_InitStructure.USART_StopBits = USART_StopBits_1;
	USART_InitStructure.USART_Parity = USART_Parity_No;
	USART_InitStructure.USART_HardwareFlowControl = USART_HardwareFlowControl_None;
	USART_InitStructure.USART_Mode = USART_Mode_Rx | USART_Mode_Tx;
	USART_Init(USART2, &USART_InitStructure);
	
	//中断优先级设置
	NVIC_InitStructure.NVIC_IRQChannel = USART2_IRQn;
	NVIC_InitStructure.NVIC_IRQChannelPreemptionPriority = 3;
	NVIC_InitStructure.NVIC_IRQChannelSubPriority = 0;
	NVIC_InitStructure.NVIC_IRQChannelCmd = ENABLE;
	NVIC_Init(&NVIC_InitStructure);
	
	//中断标志设置
	USART_ITConfig(USART2, USART_IT_TXE, DISABLE);
	USART_ITConfig(USART2, USART_IT_RXNE, ENABLE);
	
	//清除标志位并使能
	USART_ClearFlag(USART2,USART_FLAG_TC);	
	USART_Cmd(USART2, ENABLE);
	
}


uint8_t TxBuffer2[256];
uint8_t RxBuffer2[256];
uint8_t cnt2=0;
uint8_t TxCnt2=0;
uint8_t RxCnt2=0;

uint8_t StartReceiveFlag=0;
uint16_t count=0;
uint16_t distance[1000];


void Usart2_CopeSerial2Data(uint8_t ucData)
{
	uint8_t i,checksum=0;
	
	if(StartReceiveFlag == 0)
	{
		return;
	}
	
	RxBuffer2[RxCnt2++]=ucData;
	if( RxBuffer2[0]!=0x5A ) //数据头不对，则重新开始寻找0x5A数据头
	{
		RxCnt2=0;
		return;
	}
	if( RxCnt2==2 && RxBuffer2[1]!=0x5A ) //数据头不对，则重新开始寻找0x5A数据头
	{
		RxBuffer2[0]=0;
		RxCnt2=0;
		return;
	}
	if(RxCnt2<8)//数目不够
	{
		return;
	}
	for(i=0;i<7;i++)
	{
		checksum += RxBuffer2[i];
	}
	checksum = checksum&0xFF;
	if( checksum!=RxBuffer2[7] )
	{
		RxCnt2=0;
		return;
	}
	else
	{
		distance[count++] = RxBuffer2[4]<<8 | RxBuffer2[5];
		RxCnt2=0;
	}
	
}


//USART2中断
void USART2_IRQHandler(void)
{
	//发送数据
	if(USART_GetITStatus(USART2, USART_IT_TXE) != RESET)
	{   
		USART_SendData(USART2, TxBuffer2[TxCnt2++]); 
		USART_ClearITPendingBit(USART2, USART_IT_TXE);
		if(TxCnt2 == cnt2)
			USART_ITConfig(USART2, USART_IT_TXE, DISABLE);
	}
	//接收数据
	else if(USART_GetITStatus(USART2, USART_IT_RXNE) != RESET)
	{
		Usart2_CopeSerial2Data((unsigned char)USART2->DR);//处理数据
		USART_ClearITPendingBit(USART2, USART_IT_RXNE);
	}

	USART_ClearITPendingBit(USART2, USART_IT_ORE);
}



void USART2_Put_Char(uint8_t DataToSend)
{
	TxBuffer2[cnt2++] = DataToSend;  
	USART_ITConfig(USART2, USART_IT_TXE, ENABLE);
}


